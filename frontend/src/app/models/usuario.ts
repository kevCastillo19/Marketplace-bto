export class Usuario {
    idUsuario?:number= 0;
    nombreUsuario:string = '';
    correoUsuario:string = '';
    contrasena:string = '';
    telefonoUsuario:string = '';
    direccion:string = '';
    idRol:number = 0;

    constructor() {
    this.idUsuario=this.idUsuario;
    this.nombreUsuario=this.nombreUsuario;
    this.correoUsuario=this.correoUsuario;
    this.contrasena=this.contrasena;
    this.telefonoUsuario=this.telefonoUsuario;
    this.direccion=this.direccion;
    this.idRol = this.idRol;
    }
}